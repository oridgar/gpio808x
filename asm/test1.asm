[BITS 16]
CPU 8086
ORG 0X0000

	MOV SP, 0xFFFF
	MOV AX, CS
	MOV SS, AX
	ADD AX, 0x1000
	MOV DS, AX
	STI
INIT:
	MOV DX, 0
	MOV AX, [0XF004]
MULTIPLY:
	MUL word [0xF006]
	MOV [0xF000], AX
	MOV [0xF002], DX
	MOV BL, AL
	MOV AL, 1
	OUT 0x0, AL
WAIT_FOR_TESTER:
	IN AL, 0x0
	CMP AL, 0
	JNE WAIT_FOR_TESTER
	MOV AL, BL
	CMP DX, 0
	JNE INIT
	JMP MULTIPLY
